CREATE TABLE BULK_FILES
(
  ID_BULK_FILE INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  FK_ID_BULK INT NOT NULL,
  UNIQUE_ID VARCHAR(64) NOT NULL,
  FK_ID_USER INT NOT NULL,
  FILE_NAME VARCHAR(128) NOT NULL,
  FILE_ORIG_NAME VARCHAR(128) NOT NULL,
  FILE_TYPE TINYINT NOT NULL,
  ID_SIGN INT NOT NULL,
  FULL_NAME VARCHAR(128) NOT NULL,
  FILE_DATE DATE NOT NULL,
  FK_ID_CONTENT INT,
  QUALIFICATION VARCHAR(1) COMMENT 'A-F',
  HASH VARCHAR(64) NOT NULL,
  SIZE INT NOT NULL,
  CTRL_DATE DATETIME NOT NULL,
  CTRL_IP VARCHAR(128) NOT NULL,
  STATUS VARCHAR(1) NOT NULL DEFAULT 'A' COMMENT 'A - Active | T - Trash | D - Deleted',
  CTRL_DATE_DEL DATETIME,
  CTRL_IP_DEL VARCHAR(128),
INDEX IX_BULK_FILES_01 (FK_ID_USER ASC, FK_ID_BULK ASC),
UNIQUE INDEX IX_BULK_FILES_02 (HASH ASC, FK_ID_USER ASC),
UNIQUE INDEX IX_BULK_FILES_03 (UNIQUE_ID ASC)
);