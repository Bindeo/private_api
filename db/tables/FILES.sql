CREATE TABLE FILES
(
  ID_FILE INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  CLIENT_TYPE ENUM('U','C') NOT NULL COMMENT 'U - Logged user, C - Client',
  FK_ID_CLIENT INT NOT NULL,
  MODE ENUM('N', 'S') NOT NULL COMMENT 'N - Notarize, S - Sign',
  FK_ID_MEDIA INT NOT NULL,
  NAME VARCHAR(128) NOT NULL,
  FILE_NAME VARCHAR(128) NOT NULL,
  FILE_ORIG_NAME VARCHAR(128) NOT NULL,
  HASH VARCHAR(64) NOT NULL,
  SIZE INT NOT NULL,
  CTRL_DATE DATETIME NOT NULL,
  CTRL_IP VARCHAR(128) NOT NULL,
  STATUS VARCHAR(1) NOT NULL DEFAULT 'A' COMMENT 'A - Active | T - Trash',
  CTRL_DATE_DEL DATETIME,
  CTRL_IP_DEL VARCHAR(128),
  TAG VARCHAR(256),
  DESCRIPTION VARCHAR(20000),
  FK_ID_BULK INT,
  TRANSACTION VARCHAR(64),
  CONFIRMED TINYINT NOT NULL DEFAULT 0 COMMENT '0 - No, 1 - Yes',
  ID_GEONAMES INT,
  LATITUDE DECIMAL(8, 5),
  LONGITUDE DECIMAL(8, 5),
INDEX IX_FILES_01 (FK_ID_CLIENT ASC, FK_ID_MEDIA ASC),
UNIQUE INDEX IX_FILES_02 (HASH ASC, CLIENT_TYPE ASC, FK_ID_CLIENT ASC),
FULLTEXT KEY IX_FILES_03 (NAME, TAG, DESCRIPTION)
);