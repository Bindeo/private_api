CREATE TABLE BLOCKCHAIN
(
  TRANSACTION VARCHAR(64) NOT NULL PRIMARY KEY,
  NET ENUM('bitcoin') NOT NULL,
  FK_ID_USER INT NOT NULL,
  HASH VARCHAR(64) NOT NULL,
  CTRL_DATE DATETIME NOT NULL,
  CTRL_IP VARCHAR(128) NOT NULL,
  TYPE ENUM('F') NOT NULL COMMENT 'F - File',
  FK_ID_ELEMENT INT NOT NULL,
  ID_GEONAMES INT,
  LATITUDE DECIMAL(8, 5),
  LONGITUDE DECIMAL(8, 5),
  INDEX IX_BLOCKCHAIN_01 (FK_ID_USER ASC)
);